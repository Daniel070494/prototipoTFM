package com.aplicacionTFM.domain;

public class SesionActiva {
	
	  private int gnUsuario;
	  private String gsActorNombre;
	  private String gsRol;
	  private String gsRFC;
	  private String gsEmail;
	  private String gsTelefono;
	  
	  
	public int getGnUsuario() {
		return gnUsuario;
	}
	public void setGnUsuario(int gnUsuario) {
		this.gnUsuario = gnUsuario;
	}
	public String getGsActorNombre() {
		return gsActorNombre;
	}
	public void setGsActorNombre(String gsActorNombre) {
		this.gsActorNombre = gsActorNombre;
	}
	public String getGsRol() {
		return gsRol;
	}
	public void setGsRol(String gsRol) {
		this.gsRol = gsRol;
	}
	public String getGsRFC() {
		return gsRFC;
	}
	public void setGsRFC(String gsRFC) {
		this.gsRFC = gsRFC;
	}
	public String getGsEmail() {
		return gsEmail;
	}
	public void setGsEmail(String gsEmail) {
		this.gsEmail = gsEmail;
	}
	public String getGsTelefono() {
		return gsTelefono;
	}
	public void setGsTelefono(String gsTelefono) {
		this.gsTelefono = gsTelefono;
	}


}
